package StringOperations;

public class StringCreation {
    public static void main(String[] args) {
        String str1 = "Hello";
        String str2 = new String("World");
        String str3 = new String(new char[]{'J', 'a', 'v', 'a'});

        System.out.println(str1 + "\n" + str2 + "\n" + str3);
    }
}
